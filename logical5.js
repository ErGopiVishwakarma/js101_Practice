let day="wed";
switch(day){
  case "sun":
    console.log("sunday");
    break;
  case "mon":
    console.log("monday");
    break;
  case "tues":
    console.log("tuesday");
    break;
  case "wed":
    console.log("wednesday");
    break;
  case "thu":
    console.log("thursday");
    break;
  case "fri":
    console.log("friday");
    break;
  case "sat":
    console.log("saturday");
    break;
  default:
    console.log("wrong input");
}